
# 和math相关的api

import math
from pprint import pprint

if __name__ == "__main__":
    # 求阶乘
    pprint(math.factorial(6))
    # 向下取整
    pprint(math.floor(12.23))
    # 向上取整
    pprint(math.ceil(15.001))
    
    pprint(math.sin(math.pi / 6))

    pprint(math.cos(math.pi / 6))

    pprint(math.tan(math.pi / 4))
